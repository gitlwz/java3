package cn.shopping;

public class shopp {
	int num;
	String name;
	String type;
	Double price;
	public shopp(int num, String name, String type, Double price) {
		this.num = num;
		this.name = name;
		this.type = type;
		this.price = price;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	
	
	
	
}
